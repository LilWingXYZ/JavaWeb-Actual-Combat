package com.hp.day5.fortest;

public class Rectangle {

	public static void main(String[] args) {
		// 循环迭代变量i,j,k
		/*
		 * System.out.println在控制台打印括号里的内容，ln换行
		 * System.out.print不换行
		 */
		for(int i = 0; i < 5; i++) {
			for (int j = 0; j < 8; j++) {
				System.out.print("*");
			}
			System.out.println();
			
		}

	}

}
