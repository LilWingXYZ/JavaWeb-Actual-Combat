package com.hp.day7.forum;

public class Statistics {
	private int userQuantity;
	private int postQuantity;
	private int replyQuantity;
	
	public int getUserQuantity() {
		return userQuantity;
	}
	public void setUserQuantity(int userQuantity) {
		this.userQuantity = userQuantity;
	}
	public int getPostQuantity() {
		return postQuantity;
	}
	public void setPostQuantity(int postQuantity) {
		this.postQuantity = postQuantity;
	}
	public int getReplyQuantity() {
		return replyQuantity;
	}
	public void setReplyQuantity(int replyQuantity) {
		this.replyQuantity = replyQuantity;
	}

	
}
